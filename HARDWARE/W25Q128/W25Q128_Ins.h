#define W25Q128_FAST_READ_QUAD_IO						0xEB
#define W25Q128_OCTAL_WORD_READ_QUAD_IO				0xE3

#define W25Q128_DUMMY_BYTE								0xFF  // 驱动中SPI交换哑元字节会用到此宏

#endif